#!/bin/bash

# --- –ö–û–ù–§–ò–ì–£–†–ê–¶–ò–Ø ---
# –ë–∞–∑–æ–≤–∞—è —Å—Å—ã–ª–∫–∞ –Ω–∞ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π
REPO_BASE_URL="https://raw.githubusercontent.com/PavloMakaro/Vpnbot/main"
# –ò–º—è —Ñ–∞–π–ª–∞ –±–æ—Ç–∞ –≤ –≤–∞—à–µ–º —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–∏
BOT_FILE="ai_studio_code.py" 
# –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è –¥–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ (–±—É–¥–µ—Ç —É–¥–∞–ª–µ–Ω–∞ –∏ —Å–æ–∑–¥–∞–Ω–∞ –∑–∞–Ω–æ–≤–æ –ø—Ä–∏ –∫–∞–∂–¥–æ–º –∑–∞–ø—É—Å–∫–µ)
INSTALL_DIR="/root/vpnbot_data"
# –ò–º—è —Å–µ—Å—Å–∏–∏ screen
SCREEN_NAME="vpn_bot_telebot"

echo "=================================================="
echo "üöÄ VPN Telegram Bot (Telebot): –°–∫—Ä–∏–ø—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–π —É—Å—Ç–∞–Ω–æ–≤–∫–∏"
echo "=================================================="

# 1. –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–∏—Å—Ç–µ–º–Ω—ã—Ö –ø–∞–∫–µ—Ç–æ–≤ –∏ —É—Å—Ç–∞–Ω–æ–≤–∫–∞
echo "–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–ø–∏—Å–∫–∞ –ø–∞–∫–µ—Ç–æ–≤ –∏ —É—Å—Ç–∞–Ω–æ–≤–∫–∞ git, screen, wget, python3-full..."
sudo apt update
sudo apt install -y python3-full python3-venv git screen wget

# 2. –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –∏ –ø–µ—Ä–µ—Ö–æ–¥ –≤ –Ω–µ–µ (—É–¥–∞–ª–µ–Ω–∏–µ —Å—Ç–∞—Ä–æ–π)
if [ -d "$INSTALL_DIR" ]; then
    echo "‚ö†Ô∏è –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è $INSTALL_DIR —Å—É—â–µ—Å—Ç–≤—É–µ—Ç. –£–¥–∞–ª—è–µ–º –¥–ª—è —á–∏—Å—Ç–æ–π —É—Å—Ç–∞–Ω–æ–≤–∫–∏..."
    rm -rf "$INSTALL_DIR"
fi

echo "–°–æ–∑–¥–∞–Ω–∏–µ –Ω–æ–≤–æ–π –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ $INSTALL_DIR..."
mkdir -p "$INSTALL_DIR"
cd "$INSTALL_DIR" || { echo "‚ùå –û—à–∏–±–∫–∞: –ù–µ —É–¥–∞–ª–æ—Å—å –ø–µ—Ä–µ–π—Ç–∏ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é $INSTALL_DIR. –í—ã—Ö–æ–¥."; exit 1; }

# 3. –ó–∞–≥—Ä—É–∑–∫–∞ —Ñ–∞–π–ª–∞ –±–æ—Ç–∞
echo "–ó–∞–≥—Ä—É–∑–∫–∞ —Ñ–∞–π–ª–∞ –±–æ—Ç–∞ ($BOT_FILE) –∏–∑ GitHub..."
wget -O "$BOT_FILE" "$REPO_BASE_URL/$BOT_FILE"

if [ $? -ne 0 ]; then
    echo "‚ùå –û—à–∏–±–∫–∞: –ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–≥—Ä—É–∑–∏—Ç—å —Ñ–∞–π–ª –±–æ—Ç–∞. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –∏–º—è —Ñ–∞–π–ª–∞ ($BOT_FILE) –≤ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–∏."
    exit 1
fi

# 4. –°–æ–∑–¥–∞–Ω–∏–µ –∏ –∞–∫—Ç–∏–≤–∞—Ü–∏—è –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è (venv)
echo "–°–æ–∑–¥–∞–Ω–∏–µ –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è..."
python3 -m venv venv

# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ pyTelegramBotAPI
echo "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å–æ–≤–º–µ—Å—Ç–∏–º–æ–π –±–∏–±–ª–∏–æ—Ç–µ–∫–∏ (pyTelegramBotAPI)..."
source venv/bin/activate
pip install pyTelegramBotAPI

if [ $? -ne 0 ]; then
    echo "‚ùå –û—à–∏–±–∫–∞: –ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å pyTelegramBotAPI. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ø—Ä–∞–≤–∞ –¥–æ—Å—Ç—É–ø–∞."
    deactivate
    exit 1
fi

# 5. –ó–∞–ø—É—Å–∫ –±–æ—Ç–∞ –≤ screen
echo "–ó–∞–ø—É—Å–∫ Telegram-–±–æ—Ç–∞ ($BOT_FILE) –≤ —Ñ–æ–Ω–æ–≤–æ–º —Ä–µ–∂–∏–º–µ (screen: $SCREEN_NAME)..."

# –ü—Ä–æ–≤–µ—Ä–∫–∞, –Ω–µ –∑–∞–ø—É—â–µ–Ω –ª–∏ —É–∂–µ screen —Å —Ç–∞–∫–∏–º –∏–º–µ–Ω–µ–º, –∏ –æ—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å—Ç–∞—Ä–æ–π —Å–µ—Å—Å–∏–∏
screen -ls | grep -q "$SCREEN_NAME"
if [ $? -eq 0 ]; then
    echo "‚ÑπÔ∏è –°–µ—Å—Å–∏—è screen '$SCREEN_NAME' —É–∂–µ –∑–∞–ø—É—â–µ–Ω–∞. –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Å—Ç–∞—Ä—É—é..."
    screen -X -S "$SCREEN_NAME" quit
fi

# –ó–∞–ø—É—Å–∫ –±–æ—Ç–∞
screen -dmS "$SCREEN_NAME" bash -c "source $INSTALL_DIR/venv/bin/activate && python3 $BOT_FILE"

echo "=================================================="
echo "‚úÖ –ë–æ—Ç —É—Å–ø–µ—à–Ω–æ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –∏ –∑–∞–ø—É—â–µ–Ω –≤ —Ñ–æ–Ω–æ–≤–æ–º —Ä–µ–∂–∏–º–µ!"
echo "   –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏: $INSTALL_DIR"
echo "--------------------------------------------------"
echo "üíª –ö–æ–º–∞–Ω–¥—ã –¥–ª—è —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –±–æ—Ç–æ–º:"
echo "1. –ó–∞–π—Ç–∏ –≤ –∫–æ–Ω—Å–æ–ª—å –±–æ—Ç–∞ (–ø—Ä–æ–≤–µ—Ä–∏—Ç—å –ª–æ–≥–∏): screen -r $SCREEN_NAME"
echo "2. –í—ã–π—Ç–∏ –∏–∑ screen (–Ω–µ –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞—è –±–æ—Ç–∞): –ù–∞–∂–º–∏—Ç–µ Ctrl+A, –∑–∞—Ç–µ–º D"
echo "=================================================="

exit 0
